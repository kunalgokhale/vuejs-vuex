<template>
  <div id="registration">
    <h3>Register here</h3>
    <hr />
    <div class="row m-1" v-for="user in users" v-bind:key="user.id">
      <h4>{{ user.name }}</h4>
      <button @click="registerUser(user)">Register</button>
    </div>
  </div>
</template>

<script>
export default {
  computed: {
    users() {
      return this.$store.getters.unregisteredUsers;
    },
  },
  methods: {
    registerUser(user) {
     this.$store.commit('registerUser', user.id);
    },
  },
};
</script>

